<script>
function toggleFaq(button) {
    // Tüm FAQ öğelerini bul
    const allFaqs = document.querySelectorAll('#faq button');
    const allAnswers = document.querySelectorAll('#faq button + div');
    const allIcons = document.querySelectorAll('#faq button svg');

    // Tıklanan öğenin cevap kısmını bul
    const answer = button.nextElementSibling;
    const icon = button.querySelector('svg');

    // Eğer tıklanan öğe zaten açıksa, kapat
    if (button.getAttribute('aria-expanded') === 'true') {
        button.setAttribute('aria-expanded', 'false');
        answer.classList.add('hidden');
        icon.classList.remove('rotate-180');
        return;
    }

    // Diğer tüm açık öğeleri kapat
    allFaqs.forEach((faq, index) => {
        faq.setAttribute('aria-expanded', 'false');
        allAnswers[index].classList.add('hidden');
        allIcons[index].classList.remove('rotate-180');
    });

    // Tıklanan öğeyi aç
    button.setAttribute('aria-expanded', 'true');
    answer.classList.remove('hidden');
    icon.classList.add('rotate-180');
}
</script>
@endpush
